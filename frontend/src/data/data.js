function generateProducts() {
    const generated = [];

    for (let i = 0; i < exactProductsData.length; i++) {
        const item = exactProductsData[i];

        const city1 = indianCities[i % indianCities.length];
        const city2 = indianCities[(i + 5) % indianCities.length];
        const hasLocal = i % 3 !== 0; // Roughly 66% have a local option

        generated.push({
            id: i + 1,
            name: item.name,
            category: item.cat,
            price: item.price,
            image: `/images/products/${item.img}.webp`,
            sourceWarehouse: city1,
            localWarehouse: hasLocal ? city2 : null,
            localAvailableDays: hasLocal ? Math.floor(i / 10) + 1 : 0
        });
    }

    return generated;
}

